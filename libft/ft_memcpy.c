#include "libft.h"

void			*ft_memcpy(void *cop, const void *src, size_t n)
{
	int			i;

	i = 0;
	while (n--)
	{
		((char *)cop)[i] = ((char *)src)[i];
		i++;
	}
	return (cop);
}
