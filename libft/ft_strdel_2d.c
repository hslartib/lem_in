#include "libft.h"

void	*ft_strdel_2d(char ***ap)
{
	int		i;

	i = -1;
	if (!ap || !*ap)
		return (NULL);
	while (ap[0][++i])
	{
		free(ap[0][i]);
		ap[0][i] = NULL;
	}
	free(*ap);
	*ap = NULL;
	return (NULL);
}
